int32_t
TranslatePixelCode16CLUT(uint8_t        pixel_code,
                         uint8_t        compatibility,
                         uint8_t        depth,
                         LS_ColorYUV_t* CLUT16,
                         uint8_t*       maptable2to4bit,
                         uint8_t*       maptable8to4bit,
                         LS_ColorYUV_t* color)
{
  LS_ColorYUV_t* entry = NULL;

  if (color == NULL)
  {
    LS_ERROR("GENERAL: NULL parameter pointer\n");
    return LS_ERROR_GENERAL;
  }

  if ((compatibility != 1) &&
      (compatibility != 2))
  {
    LS_ERROR("STREAM_DATA: compatibility = %d is not supported\n", compatibility);
    return LS_ERROR_STREAM_DATA;
  }

  if (depth == 2)
  {
    DEBUG_CHECK((pixel_code & ((~0U) << 2)) == 0);
    pixel_code = CLAMP(pixel_code, 0, 3);

    if (maptable2to4bit == NULL)
    {
      maptable2to4bit = kDefault2To4BitMapTable;
      LS_WARNING("using Default2To4BitMapTable...\n");
    }

    pixel_code = maptable2to4bit[pixel_code];
    entry = (CLUT16 == NULL ? kDefaultAYUV16CLUT : CLUT16) + pixel_code;
  }
  else if (depth == 4)
  {
    DEBUG_CHECK((pixel_code & ((~0U) << 4)) == 0);
    pixel_code = CLAMP(pixel_code, 0, 15);
    entry = (CLUT16 == NULL ? kDefaultAYUV16CLUT : CLUT16) + pixel_code;
  }
  else if (depth == 8)
  {
    DEBUG_CHECK((pixel_code & ((~0U) << 8)) == 0);
    pixel_code = CLAMP(pixel_code, 0, 255);

    if (maptable8to4bit == NULL)
    {
      maptable8to4bit = kDefault8To4BitMapTable;
    }

    pixel_code = maptable8to4bit[pixel_code];
    entry = (CLUT16 == NULL ? kDefaultAYUV16CLUT : CLUT16) + pixel_code;
  }
  else
  {
    LS_ERROR("STREAM_DATA: depth = %d is not supported!\n", depth);
    return LS_ERROR_STREAM_DATA;
  }

  DEBUG_CHECK(entry);
  SYS_MEMSET((void*)color, 0, sizeof(LS_ColorYUV_t));

  if (entry)
  {
    SYS_MEMCPY((void*)color, (void*)entry, sizeof(LS_ColorYUV_t));
    return LS_OK;
  }
  else
  {
    LS_ERROR("STREAM_DATA: empty entry found!\n");
    return LS_ERROR_STREAM_DATA;
  }
}


